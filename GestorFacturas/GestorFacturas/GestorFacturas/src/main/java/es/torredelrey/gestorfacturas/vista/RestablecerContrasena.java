    private void btnCrearCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearCuentaActionPerformed
        boolean salir = false;
        if(txfUsuario.getText().isBlank() || txfUsuario.getText().isEmpty() || txfUsuario.getForeground()==Color.red){
            salir=true;
            txfUsuario.setForeground(Color.red);
            txfUsuario.setText("Introduce un usuario");
        }
        
        if(txfContrasena1.getText().isBlank() || txfContrasena1.getText().isEmpty() || txfContrasena1.getForeground()==Color.red){
            salir=true;
            txfContrasena1.setForeground(Color.red);
            txfContrasena1.setText("Introduce una contraseña");
        }
        
        if(!txfContrasena1.getText().equals(txfContrasena2.getText()) || txfContrasena2.getForeground()==Color.red){
            salir=true;
            txfContrasena2.setForeground(Color.red);
            txfContrasena2.setText("Las contraseñas deben ser iguales");
        }
        
        if(salir) return;
        
        ArrayList<Usuario> usuarios = new ArrayList<>(ujc.findUsuarioEntities());
        for(Usuario u : usuarios){
            if(u.getUsuario().equals(txfUsuario.getText())){
                u.setPassword(txfContrasena1.getText());
                try {
                    ujc.edit(u);
                } catch (Exception ex) {
                    Logger.getLogger(RestablecerContrasena.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(this,"Contraseña cambiada con éxito");
                this.dispose();
                return;
            }
        }

        JOptionPane.showMessageDialog(this,"Ese usuario no existe");
    }//GEN-LAST:event_btnCrearCuentaActionPerformed

